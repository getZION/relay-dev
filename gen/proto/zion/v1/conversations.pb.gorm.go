// ToORM runs the BeforeToORM hook if present, converts the fields of this
// object to ORM format, runs the AfterToORM hook, then returns the ORM object
func (m *Conversation) ToORM(ctx context.Context) (ConversationORM, error) {
	to := ConversationORM{}
	var err error
	if prehook, ok := interface{}(m).(ConversationWithBeforeToORM); ok {
		if err = prehook.BeforeToORM(ctx, &to); err != nil {
			return to, err
		}
	}
	to.Id = m.Id
	to.Zid = m.Zid
	to.CommunityZid = m.CommunityZid
	to.Text = m.Text
	to.Link = m.Link
	to.Img = m.Img
	to.Video = m.Video
	to.Public = m.Public
	to.PublicPrice = m.PublicPrice
	to.Deleted = m.Deleted
	to.Created = m.Created
	to.Updated = m.Updated
	for _, v := range m.Comments {
		if v != nil {
			if tempComments, cErr := v.ToORM(ctx); cErr == nil {
				to.Comments = append(to.Comments, &tempComments)
			} else {
				return to, cErr
			}
		} else {
			to.Comments = append(to.Comments, nil)
		}
	}
	if posthook, ok := interface{}(m).(ConversationWithAfterToORM); ok {
		err = posthook.AfterToORM(ctx, &to)
	}
	return to, err
}

